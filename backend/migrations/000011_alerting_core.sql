  event_type text NOT NULL,
  actor_subject_type text NOT NULL DEFAULT 'system',
  actor_subject_id text NOT NULL DEFAULT 'system',
  metadata_json jsonb NOT NULL DEFAULT '{}'::jsonb,
  occurred_at timestamptz NOT NULL DEFAULT now(),
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT alert_events_type_check CHECK (event_type IN ('created', 'updated', 'resolved', 'acknowledged', 'snoozed'))
);

CREATE INDEX alert_events_alert_created_idx ON alert_events(alert_id, created_at DESC);

